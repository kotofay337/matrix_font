String utf8_866rus(char * source)
{
  int i,k;
  String target;
  unsigned char n;
  char m[2] = { '0', '\0' };

  k = strlen(source); i = 0;

  while (i < k) {
    n = source[i]; i++;

    // decode utf8 to 1251
    if (n >= 0xC0) {
      switch (n) {
        case 0xD0: {
          n = source[i]; i++;
          if (n == 0x81) { n = 0xA8; break; }
          if (n >= 0x90 && n <= 0xBF) n = n + 0x30;
          break;
        }
        case 0xD1: {
          n = source[i]; i++;
          if (n == 0x91) { n = 0xB8; break; }
          if (n >= 0x80 && n <= 0x8F) n = n + 0x70;
          break;
        }
      }
    }
    
    // decode 1251 to 866
    if( n >= 0xC0 && n < 0xF0 )
       n -= 0x40;
    else if( n >= 0xF0 && n <= 0xFF )
       n -= 0x10;

    m[0] = n; 
    target = target + String(m);
  }
  return target;
}

    MatrixFont::MatrixFont( Adafruit_GFX * r ){
      SYMBOL_WIDTH = 8;
      bg = 0; fg = 0xFFFF;
      h = 16;//sizeof( DOS_866_8x16[0] );
      raster = r;
    }

    void MatrixFont::putPixel( bool dot ){
        if( x < raster->width() && y < raster->height() ) {
            auto c = dot ? fg : bg;
            raster->writePixel(x, y, c);
        }
        x++;
    }

    void MatrixFont::putScanline( int row ) {
        int x_ = x;
        for( int nextChar = 0, length = strlen( (const char *)currentPrinting ); nextChar < length; nextChar++ ) {
            auto code = currentPrinting[ nextChar ];
            auto bitmap = DOS_866_8x16;
            //if( code > bitmap.length )
            //    code = 0;
            auto dots = bitmap[code][row];
            for (int i = SYMBOL_WIDTH; i >= 0; i--) {
                auto dot = ((dots >> i) & 1) != 0;
                putPixel(dot);
            }
        };
        x = x_;
    }

    void MatrixFont::drawString( char * s, int x, int y ){
        raster->startWrite();
        currentPrinting = (unsigned char *)utf8_866rus( s ).c_str();
        this->y = y;
        this->x = x;
        for( int row = 0; row < h; row++, this->y++ ) {
            putScanline(row);
        }
        raster->setCursor(this->x, this->y);
        raster->endWrite();
    }

    size_t MatrixFont::printf( uint16_t fg, uint16_t bg, const char * format, ... ){
      va_list arg;
      va_start(arg, format);

      raster->setTextColor(fg, bg);     
      this->fg = fg;
      this->bg = bg;

      int16_t x = raster->getCursorX();
      int16_t y = raster->getCursorY();

      char temp[64];
      char* buffer = temp;
      size_t len = vsnprintf(temp, sizeof(temp), format, arg);
      va_end(arg);
      if (len > sizeof(temp) - 1) {
          buffer = new char[len + 1];
          if (!buffer) {
              return 0;
          }
          va_start(arg, format);
          vsnprintf(buffer, len + 1, format, arg);
          va_end(arg);
      }

      drawString( buffer, x, y );

      if (buffer != temp) {
          delete[] buffer;
      }

      return len;
    }
